#      –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –≤ –ø–µ—Ä–≤–æ–π —Å—Ç—Ä–æ–∫–µ –¥–∞–µ—Ç—Å—è —á–∏—Å–ª–æ m ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —É—Ä–æ–∫–æ–≤, –ø—Ä–æ–≤–µ–¥–µ–Ω–Ω—ã—Ö —Å –Ω–∞—á–∞–ª–∞ —É—á–µ–±–Ω–æ–≥–æ –≥–æ–¥–∞. –î–∞–ª–µ–µ –∏–¥—ë—Ç m –±–ª–æ–∫–æ–≤ —Å—Ç—Ä–æ–∫, –æ–ø–∏—Å—ã–≤–∞—é—â–∏—Ö –ª–∏—Å—Ç–∫–∏ —Å —Ñ–∞–º–∏–ª–∏—è–º–∏. 
#      –ù–∞ –ø–µ—Ä–≤–æ–π —Å—Ç—Ä–æ–∫–µ –∫–∞–∂–¥–æ–≥–æ –±–ª–æ–∫–∞ —É–∫–∞–∑–∞–Ω–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ñ–∞–º–∏–ª–∏–π ni, –∑–∞—Ç–µ–º –∏–¥—ë—Ç ni —Å—Ç—Ä–æ—á–µ–∫ —Å —Ñ–∞–º–∏–ª–∏—è–º–∏ —Ç–µ—Ö, –∫—Ç–æ –±—ã–ª –Ω–∞ i-–æ–º —É—Ä–æ–∫–µ.
