#      –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –≤ –ø–µ—Ä–≤—ã—Ö –¥–≤—É—Ö —Å—Ç—Ä–æ–∫–∞—Ö –ø–æ–¥–∞—é—Ç—Å—è —á–∏—Å–ª–∞ m –∏ n ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ —É—á–µ–Ω–∏–∫–æ–≤, –∏–∑—É—á–∞—é—â–∏—Ö –º–∞—Ç–µ–º–∞—Ç–∏–∫—É –∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ç–∏–∫—É —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ. 
#      –î–∞–ª–µ–µ –∏–¥—É—Ç m+n —Å—Ç—Ä–æ–∫ ‚Äî —Ñ–∞–º–∏–ª–∏–∏ —É—á–µ–Ω–∏–∫–æ–≤, –∏–∑—É—á–∞—é—â–∏—Ö –º–∞—Ç–µ–º–∞—Ç–∏–∫—É –∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ç–∏–∫—É, –≤ –ø—Ä–æ–∏–∑–≤–æ–ª—å–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ.
# 
#      –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#      –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —É—á–µ–Ω–∏–∫–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –∏–∑—É—á–∞—é—Ç —Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω –ø—Ä–µ–¥–º–µ—Ç. –ï—Å–ª–∏ —Ç–∞–∫–∏—Ö —É—á–µ–Ω–∏–∫–æ–≤ –Ω–µ –æ–∫–∞–∂–µ—Ç—Å—è, —Ç–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –≤—ã–≤–µ—Å—Ç–∏ NO.
# 
#      –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ì–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç—Å—è, —á—Ç–æ —Å—Ä–µ–¥–∏ —É—á–µ–Ω–∏–∫–æ–≤ —à–∫–æ–ª—ã BEEGEEK –Ω–µ—Ç –æ–¥–Ω–æ—Ñ–∞–º–∏–ª—å—Ü–µ–≤.

m, n = int(input()), int(input())
st = set()

for _ in range(m + n):
    if (x := input()) not in st:
        st.add(x)
    else:
        st.discard(x)
        
print(len(st) if st else 'NO')

