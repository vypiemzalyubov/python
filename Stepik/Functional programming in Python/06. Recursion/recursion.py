# –ü–µ—Ä–µ–ø–∏—à–∏—Ç–µ –¥–∞–Ω–Ω—É—é –ø—Ä–æ–≥—Ä–∞–º–º—É –ø—Ä–∏ –ø–æ–º–æ—â–∏ —Ä–µ–∫—É—Ä—Å–∏–∏

def gcd(a, b):
    if b != 0:
        a, b = b, a % b
        return gcd(a, b)
