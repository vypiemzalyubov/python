# –∏ —Ä–∞—Å–ø–æ–ª–∞–≥–∞–µ—Ç –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã –Ω–∞ –∫–∞–∂–¥–æ–º —É—Ä–æ–≤–Ω–µ –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–∏ .–í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ–π —Ñ—É–Ω–∫—Ü–∏–∏ reversed_recursive

def reversed_recursive(lst):
    result = []
    for i in lst:
        if isinstance(i, list):
            tmp = reversed_recursive(i)
            result.append(tmp)
        elif isinstance(i, int):
            result.append(i)
    result.reverse()
    return result

