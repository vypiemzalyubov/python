# –ü–µ—Ä–µ–ø–∏—à–∏—Ç–µ —Ä–µ–∞–ª–∏–∑–∞—Ü–∏—é —Ñ—É–Ω–∫—Ü–∏–∏ power —Å –∏—Ç–µ—Ä–∞—Ü–∏–∏ –Ω–∞ —Ä–µ–∫—É—Ä—Å–∏—é. –ù–∞–∑–≤–∞–Ω–∏–∏ —Ñ—É–Ω–∫—Ü–∏–∏ –∏ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –Ω–µ –¥–æ–ª–∂–Ω—ã –º–µ–Ω—è—Ç—å—Å—è

def power(a, n):
    if n < 1:
        return 1
    return a * power(a, n - 1)

