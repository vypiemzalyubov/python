# –í–æ –≤—Ä–µ–º—è –ø—Ä–æ–≤–µ—Ä–æ–∫ —Ä–µ–≥–∏—Å—Ç—Ä –±—É–∫–≤ –Ω–µ —É—á–∏—Ç—ã–≤–∞–π—Ç–µ. –í —Ç–µ—Å—Ç–æ–≤—ã—Ö –¥–∞–Ω–Ω—ã—Ö –∏—Å–ø–æ–ª—å–∑—É—é—Ç—Å—è —Ç–æ–ª—å–∫–æ —Å–∏–º–≤–æ–ª—ã –∞–Ω–≥–ª–∏–π—Å–∫–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞. –ó–Ω–∞–∫–∏ –ø—É–Ω–∫—Ç—É–∞—Ü–∏–∏ –∏ –ø—Ä–æ–±–µ–ª—ã –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç.

def is_palindrome(s):
    s = s.lower()
    if len(s) < 2:
        return True
    if s[0] == s[-1]:
        return is_palindrome(s[1:-1])
    return False

