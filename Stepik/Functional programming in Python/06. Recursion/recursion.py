# –ö–ª—é—á–∞–º–∏ —Å–ª–æ–≤–∞—Ä—è –Ω–∞ –ª—é–±–æ–º —É—Ä–æ–≤–Ω–µ –º–æ–≥—É—Ç –±—ã—Ç—å —Ç–æ–ª—å–∫–æ —Å—Ç—Ä–æ–∫–∏, –∑–Ω–∞—á–µ–Ω–∏—è–º–∏ - —Ç–æ–ª—å–∫–æ —á–∏—Å–ª–∞.
# –£—á–∏—Ç—ã–≤–∞—è —É–∫–∞–∑–∞–Ω–Ω—ã–µ –≤—ã—à–µ —É—Å–ª–æ–≤–∏—è, –≤–∞—à–∞ –∑–∞–¥–∞—á–∞ —Å–æ—Å—Ç–æ–∏—Ç –≤ —Ç–æ–º, —á—Ç–æ–±—ã –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞—Ç—å —ç—Ç–æ—Ç –≤–ª–æ–∂–µ–Ω–Ω—ã–π —Å–ª–æ–≤–∞—Ä—å –≤ –ø–ª–æ—Å–∫–∏–π (—Å–æ—Å—Ç–æ—è—â–∏–π —Ç–æ–ª—å–∫–æ –∏–∑ –æ–¥–Ω–æ–≥–æ —É—Ä–æ–≤–Ω—è),
# –≥–¥–µ –∫–ª—é—á–∏ —Ñ–æ—Ä–º–∏—Ä—É—é—Ç—Å—è –∫–æ–Ω–∫–∞—Ç–µ–Ω–∞—Ü–∏–µ–π –≤–ª–æ–∂–µ–Ω–Ω—ã—Ö –∫–ª—é—á–µ–π, —Å–æ–µ–¥–∏–Ω–µ–Ω–Ω—ã—Ö –∑–Ω–∞–∫–æ–º _
# –î–ª—è —ç—Ç–æ–≥–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é flatten_dict. –û–Ω–∞ –¥–æ–ª–∂–Ω–∞ –ø—Ä–∏–Ω–∏–º–∞—Ç—å –≤–ª–æ–∂–µ–Ω–Ω—ã–π —Å–ª–æ–≤–∞—Ä—å –∏ –≤–æ–∑–≤—Ä–∞—â–∞—Ç—å –ø–ª–æ—Å–∫–∏–π
# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ flatten_dict

def flatten_dict(dct, parent_key='', sep='_'):
    result = {}
    for k, v in dct.items():
        tmp_key = f'{parent_key}{sep}{k}' if parent_key else k
        if isinstance(v, dict):
            result.update(flatten_dict(v, tmp_key, sep=sep))
        else:
            result[tmp_key] = v
    return result