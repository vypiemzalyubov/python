# –≠–ª–µ–º–µ–Ω—Ç—ã –≤ –ø–ª–æ—Å–∫–æ–º —Å–ø–∏—Å–∫–µ –¥–æ–ª–∂–Ω—ã —Ä–∞—Å–ø–æ–ª–∞–≥–∞—Ç—å—Å—è –≤ —Ç–æ–º –∂–µ –ø–æ—Ä—è–¥–∫–µ, –∫–∞–∫ –æ–Ω–∏ —Å–ª–µ–¥–æ–≤–∞–ª–∏ –≤ –∏—Å—Ö–æ–¥–Ω–æ–º —Å–ø–∏—Å–∫–µ.
# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ ‚Äî –Ω–∞–ø–∏—Å–∞—Ç—å —Ç–æ–ª—å–∫–æ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ flatten.

def flatten(lst):
    result = []
    for i in lst:
        if isinstance(i, list):
            tmp = flatten(i)
            result.extend(tmp)
        elif isinstance(i, int):
            result.append(i)
    return result

# –†–∞–Ω–µ–µ –º—ã —É–∂–µ –¥–µ–ª–∞–ª–∏ –ø—Ä–æ–≤–µ—Ä–∫—É –Ω–∞ –≤—Ö–æ–∂–¥–µ–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–∞ –≤ –ª–∏–Ω–µ–π–Ω—ã–π —Å–ø–∏—Å–æ–∫. –¢–µ–ø–µ—Ä—å –≤–∞—à–∞ –∑–∞–¥–∞—á–∞ –ø–µ—Ä–µ–ø–∏—Å–∞—Ç—å —Ñ—É–Ω–∫—Ü–∏—é is_member —Ç–∞–∫,
# —á—Ç–æ–±—ã –æ–Ω–∞ –º–æ–≥–ª–∞ –∏—Å–∫–∞—Ç—å —ç–ª–µ–º–µ–Ω—Ç –≤ —Å–ø–∏—Å–∫–µ —Å –ø—Ä–æ–∏–∑–≤–æ–ª—å–Ω–æ–π –≤–ª–æ–∂–µ–Ω–Ω–æ—Å—Ç—å—é. –§—É–Ω–∫—Ü–∏—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –Ω–µ–∫–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ value –∏ —Å–ø–∏—Å–æ–∫ –∑–Ω–∞—á–µ–Ω–∏–π lst.
# –§—É–Ω–∫—Ü–∏—è is_member –¥–æ–ª–∂–Ω–∞ –≤–µ—Ä–Ω—É—Ç—å True, –µ—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ value –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ —Å–ø–∏—Å–∫–µ lst –Ω–∞ –ª—é–±–æ–º —É—Ä–æ–≤–Ω–µ, –∏ False –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ.

def is_member(value, lst):
    def _flatten(lst):
        result = []
        for i in lst:
            if isinstance(i, list):
                tmp = _flatten(i)
                result.extend(tmp)
            elif isinstance(i, int):
                result.append(i)
        return result
    return value in _flatten(lst)

# –°–æ–∑–¥–∞–π—Ç–µ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é find_level_element, –∫–æ—Ç–æ—Ä–∞—è –æ–ø—Ä–µ–¥–µ–ª—è–µ—Ç –Ω–∞ –∫–∞–∫–æ–º —É—Ä–æ–≤–Ω–µ –≤–ª–æ–∂–µ–Ω–Ω–æ—Å—Ç–∏ —Ä–∞—Å–ø–æ–ª–∞–≥–∞–µ—Ç—Å—è –∏–Ω—Ç–µ—Ä–µ—Å—É—é—â–∏–π –Ω–∞—Å —ç–ª–µ–º–µ–Ω—Ç.
# –ù—É–º–µ—Ä–∞—Ü–∏—è —É—Ä–æ–≤–Ω–µ–π –≤–ª–æ–∂–µ–Ω–Ω–æ—Å—Ç–∏ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –µ–¥–∏–Ω–∏—Ü—ã. –§—É–Ω–∫—Ü–∏—è find_level_element –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –Ω–µ–∫–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ value –∏ —Å–ø–∏—Å–æ–∫ –∑–Ω–∞—á–µ–Ω–∏–π lst.
# –§—É–Ω–∫—Ü–∏—è find_level_element –¥–æ–ª–∂–Ω–∞ –≤–µ—Ä–Ω—É—Ç—å –Ω–æ–º–µ—Ä —É—Ä–æ–≤–Ω—è, –≥–¥–µ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è –ø–µ—Ä–≤–æ–µ –Ω–∞–π–¥–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ value –≤ —Å–ø–∏—Å–∫–µ lst –Ω–∞ –ª—é–±–æ–º —É—Ä–æ–≤–Ω–µ.
# –ï—Å–ª–∏ –∂–µ –≤ lst –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ value, —Ñ—É–Ω–∫—Ü–∏—è find_level_element –¥–æ–ª–∂–Ω–∞ –≤–µ—Ä–Ω—É—Ç—å -1.

def find_level_element(value, lst, level=1):
    if value in lst:
        return level
    for i in lst:
        if isinstance(i, list):
            result = find_level_element(value, i, level+1)
            if result:
                return result
    return -1

