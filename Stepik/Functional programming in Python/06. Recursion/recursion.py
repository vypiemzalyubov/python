# –ü–µ—Ä–µ–ø–∏—à–∏—Ç–µ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é speller —Ç–∞–∫, —á—Ç–æ–±—ã –æ–Ω–∞ –≤—ã–≤–æ–¥–∏–ª–∞ –±—É–∫–≤—ã —Å–ª–æ–≤–∞ –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ (–∫–∞–∂–¥—É—é –±—É–∫–≤—É –Ω–∞ –Ω–æ–≤–æ–π —Å—Ç—Ä–æ–∫–µ)

def speller(word):
    if len(word) > 0:
        speller(word[1:])
        print(word[0])

# –û–ø—Ä–µ–¥–µ–ª–∏—Ç–µ —Ñ—É–Ω–∫—Ü–∏—é print_from, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –æ–¥–Ω–æ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n –∏ —Ä–∞—Å–ø–µ—á–∞—Ç—ã–≤–∞–µ—Ç –Ω–∞ —ç–∫—Ä–∞–Ω–µ —É–±—ã–≤–∞—é—â—É—é –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª –æ—Ç n –¥–æ 1 –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ.
# –ö–∞–∂–¥–æ–µ —á–∏—Å–ª–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –≤—ã–≤–æ–¥–∏—Ç—å –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ. –í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ print_from

def print_from(n):
    if n < 1:
        return
    print(n)
    print_from(n - 1)

# –û–ø—Ä–µ–¥–µ–ª–∏—Ç–µ —Ñ—É–Ω–∫—Ü–∏—é print_to, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –æ–¥–Ω–æ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n –∏ —Ä–∞—Å–ø–µ—á–∞—Ç—ã–≤–∞–µ—Ç –Ω–∞ —ç–∫—Ä–∞–Ω–µ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ n –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ.
# –ö–∞–∂–¥–æ–µ —á–∏—Å–ª–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –≤—ã–≤–æ–¥–∏—Ç—å –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ. –í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ print_to

def print_to(n):
    if n > 0:
        print_to(n - 1)
        print(n)

# –ù–∞–ø–∏—à–∏—Ç–µ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é summa, –∫–æ—Ç–æ—Ä–∞—è –±—É–¥–µ—Ç —Å—É–º–º–∏—Ä–æ–≤–∞—Ç—å –≤—Å–µ —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ N. –ß–∏—Å–ª–æ N –ø–æ—Å—Ç—É–ø–∞–µ—Ç –≤–Ω—É—Ç—Ä—å —Ñ—É–Ω–∫—Ü–∏–∏ –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–∞

def summa(n):
    if n == 0:
        return 0
    return n + summa(n - 1)

# –ù–∞–ø–∏—à–∏—Ç–µ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é find_min, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞–π–¥–µ—Ç –Ω–∞–∏–º–µ–Ω—å—à–µ–µ —á–∏—Å–ª–æ –≤ —Å–ø–∏—Å–∫–µ.
# –î–ª—è —ç—Ç–æ–≥–æ —Ñ—É–Ω–∫—Ü–∏—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–∞ —Å–ø–∏—Å–æ–∫ –¥–ª—è –ø–æ–∏—Å–∫–∞ –Ω–∞–∏–º–µ–Ω—å—à–µ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è

def find_min(lst):
    if len(lst) == 0:
        return
    elif len(lst) == 1:
        return lst[0]
    return lst[0] if lst[0] < find_min(lst[1:]) else find_min(lst[1:])

# –ù–∞–ø–∏—à–∏—Ç–µ —Ñ—É–Ω–∫—Ü–∏—é sum_recursive, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –Ω–∞ –≤—Ö–æ–¥ –æ–¥–Ω–æ–º–µ—Ä–Ω—ã–π —Å–ø–∏—Å–æ–∫ –∏–∑ —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—É–º–º—É —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –ø–µ—Ä–µ–¥–∞–Ω–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞.
# –ù–µ –∑–∞–±—ã–≤–∞–π—Ç–µ, —á—Ç–æ —Ä–µ–∞–ª–∏–∑–æ–≤–∞—Ç—å —ç—Ç–æ –Ω—É–∂–Ω–æ –ø—Ä–∏ –ø–æ–º–æ—â–∏ —Ä–µ–∫—É—Ä—Å–∏–∏. –í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ sum_recursive

def sum_recursive(lst):
    if len(lst) == 0:
        return
    elif len(lst) == 1:
        return lst[0]
    return lst[0] + sum_recursive(lst[1:])

# –ù–∞–ø–∏—à–∏—Ç–µ —Ñ—É–Ω–∫—Ü–∏—é sum_digits, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞—Ö–æ–¥–∏—Ç —Å—É–º–º—É –≤—Å–µ—Ö —Ü–∏—Ñ—Ä –ø–µ—Ä–µ–¥–∞–Ω–Ω–æ–≥–æ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–≥–æ —á–∏—Å–ª–∞ n.
# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ sum_digits

def sum_digits(n):
    if n <= 0:
        return n
    return n % 10 + sum_digits(n // 10)

# –ù–µ–æ–±—Ö–æ–¥–∏–º–æ –Ω–∞–ø–∏—Å–∞—Ç—å —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é double_fact, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –Ω–∞ –≤—Ö–æ–¥ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ –∏ –≤—ã—á–∏—Å–ª—è–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ –¥–≤–æ–π–Ω–æ–≥–æ —Ñ–∞–∫—Ç–æ—Ä–∏–∞–ª–∞ –ø–æ —Ñ–æ—Ä–º—É–ª–µ.
# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ double_fact

def double_fact(n):
    if n == 1:
        return 1
    elif n == 2:
        return 2
    return n * double_fact(n - 2)

# –ü–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å—é –§–∏–±–æ–Ω–∞—á—á–∏ –Ω–∞–∑—ã–≤–∞–µ—Ç—Å—è –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å —á–∏—Å–µ–ª a0, a1, ..., an, ..., –≥–¥–µ —á–∏—Å–ª–æ, —Å—Ç–æ—è—â–µ–µ –Ω–∞ n-–æ–π –ø–æ–∑–∏—Ü–∏–∏ –º–æ–∂–Ω–æ –≤—ã—á–∏—Å–ª–∏—Ç—å –ø–æ —Ñ–æ—Ä–º—É–ª–µ.
# –¢—Ä–µ–±—É–µ—Ç—Å—è –Ω–∞–π—Ç–∏ N-–µ —á–∏—Å–ª–æ –§–∏–±–æ–Ω–∞—á—á–∏ –ø—Ä–∏ –ø–æ–º–æ—â–∏ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ–π —Ñ—É–Ω–∫—Ü–∏–∏ fibonacci. –§—É–Ω–∫—Ü–∏—è –¥–æ–ª–∂–Ω–∞ –ø—Ä–∏–Ω–∏–º–∞—Ç—å –ø–æ—Ä—è–¥–∫–æ–≤—ã–π –Ω–æ–º–µ—Ä N –∏ –≤–æ–∑–≤—Ä–∞—â–∞—Ç—å N-–æ–µ —á–∏—Å–ª–æ –§–∏–±–æ–Ω–∞—á—á–∏.
# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ fibonacci

def fibonacci(n):
    if n < 2:
        return n
    return fibonacci(n - 1) + fibonacci(n - 2)

# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ - –Ω–∞–ø–∏—Å–∞—Ç—å —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é tribonacci, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –Ω–∞ –≤—Ö–æ–¥ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ n - –ø–æ—Ä—è–¥–∫–æ–≤—ã–π –Ω–æ–º–µ—Ä —á–∏—Å–µ–ª –¢—Ä–∏–±–æ–Ω–∞—á—á–∏.
# –§—É–Ω–∫—Ü–∏—è –ø–æ –ø–∞—Ä–∞–º–µ—Ç—Ä—É n –¥–æ–ª–∂–Ω–∞ –≤—ã—á–∏—Å–ª–∏—Ç—å –∏ –≤–µ—Ä–Ω—É—Ç—å –∑–Ω–∞—á–µ–Ω–∏–µ, —Å—Ç–æ—è—â–µ–µ –Ω–∞ n-–º –º–µ—Å—Ç–µ –≤ —Ä—è–¥–µ —á–∏—Å–µ–ª –¢—Ä–∏–±–æ–Ω–∞—á—á–∏.
# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ tribonacci

def tribonacci(n):
    if n == 0 or n == 1:
        return 0
    elif n == 2:
        return 1
    return tribonacci(n - 1) + tribonacci(n - 2) + tribonacci(n - 3)

# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ - –Ω–∞–ø–∏—Å–∞—Ç—å —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é get_combin, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –Ω–∞ –≤—Ö–æ–¥ –¥–≤–∞ —Ü–µ–ª—ã—Ö —á–∏—Å–ª–∞
# –∏ –Ω–∞—Ö–æ–¥–∏—Ç C(N,K) ‚Äî —á–∏—Å–ª–æ —Å–æ—á–µ—Ç–∞–Ω–∏–π –∏–∑ N —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –ø–æ K ‚Äî —Å –ø–æ–º–æ—â—å—é —Ä–µ–∫—É—Ä—Ä–µ–Ω—Ç–Ω–æ–≥–æ —Å–æ–æ—Ç–Ω–æ—à–µ–Ω–∏—è.
# –ü—Ä–∏ —ç—Ç–æ–º –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç—Å—è, —á—Ç–æ –≤—Ö–æ–¥–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è n –∏ k –±—É–¥—É—Ç —É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä—è—Ç—å —Å–ª–µ–¥—É—é—â–∏–º —É—Å–ª–æ–≤–∏—è–º:
# - n>0
# - 0<=k<=n
# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ get_combin.
# –í —Ç–µ—Å—Ç–æ–≤—ã—Ö –ø—Ä–∏–º–µ—Ä–∞—Ö –≤ —Ñ—É–Ω–∫—Ü–∏—é get_combin —Å–ø–µ—Ä–≤–∞ –ø–µ—Ä–µ–¥–∞–µ—Ç—Å—è –∑–Ω–∞—á–µ–Ω–∏–µ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞ n, –∑–∞—Ç–µ–º k.

def get_combin(n, k):
    if k == n or k == 0:
        return 1
    elif 0 < k < n:
        return get_combin(n - 1, k) + get_combin(n - 1, k - 1)
    
# –í–∞—à–∞ –∑–∞–¥–∞—á–∞ - –Ω–∞–ø–∏—Å–∞—Ç—å —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é ackermann, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –Ω–∞ –≤—Ö–æ–¥ –¥–≤–∞ —Ü–µ–ª—ã—Ö —á–∏—Å–ª–∞  m –∏ n, –∏ –Ω–∞—Ö–æ–¥–∏—Ç –∑–Ω–∞—á–µ–Ω–∏–µ, –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω–æ–µ —Å–ª–µ–¥—É—é—â–∏–º –æ–±—Ä–∞–∑–æ–º.
# –ù–∞–π–¥–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏—è ackermann –¥–æ–ª–∂–Ω–∞ –≤–µ—Ä–Ω—É—Ç—å –≤ –∫–∞—á–µ—Å—Ç–≤–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞. –í–∞—à–∞ –∑–∞–¥–∞—á–∞ —Ç–æ–ª—å–∫–æ –Ω–∞–ø–∏—Å–∞—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ ackermann.
# –í —Ç–µ—Å—Ç–æ–≤—ã—Ö –ø—Ä–∏–º–µ—Ä–∞—Ö –≤ —Ñ—É–Ω–∫—Ü–∏—é —Å–ø–µ—Ä–≤–∞ –ø–µ—Ä–µ–¥–∞–µ—Ç—Å—è –∑–Ω–∞—á–µ–Ω–∏–µ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞ m, –∑–∞—Ç–µ–º n.

def ackermann(m, n):
    if m == 0:
        return n + 1
    elif m > 0 and n == 0:
        return ackermann(m - 1, 1)
    elif m > 0 and n > 0:
        return ackermann(m - 1, ackermann(m, n - 1))
    
# –ü–µ—Ä–µ–ø–∏—à–∏—Ç–µ —Ä–µ–∞–ª–∏–∑–∞—Ü–∏—é —Ñ—É–Ω–∫—Ü–∏–∏ is_member —á–µ—Ä–µ–∑ —Ä–µ–∫—É—Ä—Å–∏—é. –ù–∞–ø–æ–º–∏–Ω–∞—é, —Ñ—É–Ω–∫—Ü–∏—è is_member  –¥–æ–ª–∂–Ω–∞ –ø—Ä–æ–≤–µ—Ä—è—Ç—å, –µ—Å—Ç—å –ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ value –≤ –ª–∏–Ω–µ–π–Ω–æ–º —Å–ø–∏—Å–∫–µ lst.
# –§—É–Ω–∫—Ü–∏—è is_member –¥–æ–ª–∂–Ω–∞ –≤–µ—Ä–Ω—É—Ç—å True, –µ—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ value –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ —Å–ø–∏—Å–∫–µ lst, –∏ False –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ.
# –ì–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç—Å—è, —á—Ç–æ —Å–ø–∏—Å–æ–∫ lst –Ω–µ –±—É–¥–µ—Ç –≤–ª–æ–∂–µ–Ω–Ω—ã–º

def is_member(value, lst):
    if not lst:
        return False
    elif value == lst[0]:
        return True
    return is_member(value, lst[1:])

