# 195. –î–∞–Ω—ã –¥–≤–µ –¥—Ä–æ–±–∏ –≤ —Ñ–æ—Ä–º–∞—Ç–µ a/b. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—ã—á–∏—Å–ª—è–µ—Ç –∏ –≤—ã–≤–æ–¥–∏—Ç –∏—Ö —Å—É–º–º—É, —Ä–∞–∑–Ω–æ—Å—Ç—å, –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ –∏ —á–∞—Å—Ç–Ω–æ–µ.
# 
#      –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
