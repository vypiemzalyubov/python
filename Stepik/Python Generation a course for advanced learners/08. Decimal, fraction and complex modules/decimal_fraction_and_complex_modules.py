# 
#      –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –û–±—Ä–∞—Ç–∏—Ç–µ –≤–Ω–∏–º–∞–Ω–∏–µ –Ω–∞ —Ç—Ä–µ—Ç–∏–π —Ç–µ—Å—Ç: –∏—Å—Ö–æ–¥–Ω—ã–µ –¥—Ä–æ–±–∏ —Å–æ–∫—Ä–∞—â–∞—Ç—å –Ω–µ –Ω—É–∂–Ω–æ, –∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –Ω—É–∂–Ω–æ.

from fractions import Fraction

a, b = input(), input()
print(f'{a} + {b} = {Fraction(a) + Fraction(b)}')
