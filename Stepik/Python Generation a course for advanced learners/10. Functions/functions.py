#      –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ YES, –µ—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ ‚Äì —Ö–æ—Ä–æ—à–∏–π –ø–∞—Ä–æ–ª—å, –∏ NO –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ.

