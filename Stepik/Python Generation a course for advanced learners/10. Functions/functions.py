def print_products(*args):
    products = [c for c in args if isinstance(c, str) and c != '']
    if products:
        for i, v in enumerate(products, start=1):
            print(f'{i}) {v}')
    else:
        print('–ù–µ—Ç –ø—Ä–æ–¥—É–∫—Ç–æ–≤')

