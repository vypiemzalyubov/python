#      –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 4. –í—ã–∑—ã–≤–∞—Ç—å —Ñ—É–Ω–∫—Ü–∏—é greet() –Ω–µ –Ω—É–∂–Ω–æ, —Ç—Ä–µ–±—É–µ—Ç—Å—è —Ç–æ–ª—å–∫–æ —Ä–µ–∞–ª–∏–∑–æ–≤–∞—Ç—å.

def greet(name, *args):
    if not args:
        return f'Hello, {name}!'
    return f'Hello, {name} and {" and ".join([name for name in args])}!'

