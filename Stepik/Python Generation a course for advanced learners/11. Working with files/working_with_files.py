# 261. –í–∞–º –¥–æ—Å—Ç—É–ø–µ–Ω —Ç–µ–∫—Å—Ç–æ–≤—ã–π —Ñ–∞–π–ª numbers.txt –∏–∑ –¥–≤—É—Ö —Å—Ç—Ä–æ–∫, –Ω–∞ –∫–∞–∂–¥–æ–π –∏–∑ –Ω–∏—Ö –∑–∞–ø–∏—Å–∞–Ω–æ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –≤—ã–≤–æ–¥—è—â—É—é –Ω–∞ —ç–∫—Ä–∞–Ω —Å—É–º–º—É —ç—Ç–∏—Ö —á–∏—Å–µ–ª.
