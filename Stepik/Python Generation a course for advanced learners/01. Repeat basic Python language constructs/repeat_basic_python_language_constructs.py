# 8. n —á–µ–ª–æ–≤–µ–∫, –ø—Ä–æ–Ω—É–º–µ—Ä–æ–≤–∞–Ω–Ω—ã—Ö —á–∏—Å–ª–∞–º–∏ –æ—Ç 1 –¥–æ n, —Å—Ç–æ—è—Ç –≤ –∫—Ä—É–≥—É. –û–Ω–∏ –Ω–∞—á–∏–Ω–∞—é—Ç —Å—á–∏—Ç–∞—Ç—å—Å—è, –∫–∞–∂–¥—ã–π k-–π –ø–æ —Å—á–µ—Ç—É —á–µ–ª–æ–≤–µ–∫ –≤—ã–±—ã–≤–∞–µ—Ç –∏–∑ –∫—Ä—É–≥–∞, 
#    –ø–æ—Å–ª–µ —á–µ–≥–æ —Å—á–µ—Ç –ø—Ä–æ–¥–æ–ª–∂–∞–µ—Ç—Å—è —Å–æ —Å–ª–µ–¥—É—é—â–µ–≥–æ –∑–∞ –Ω–∏–º —á–µ–ª–æ–≤–µ–∫–∞. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –æ–ø—Ä–µ–¥–µ–ª—è—é—â—É—é –Ω–æ–º–µ—Ä —á–µ–ª–æ–≤–µ–∫–∞, –∫–æ—Ç–æ—Ä—ã–π –æ—Å—Ç–∞–Ω–µ—Ç—Å—è –≤ –∫—Ä—É–≥—É –ø–æ—Å–ª–µ–¥–Ω–∏–º.
# 
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —á–∏—Å–ª–∞ n –∏ k, –∑–∞–ø–∏—Å–∞–Ω–Ω—ã–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω—ã—Ö —Å—Ç—Ä–æ–∫–∞—Ö.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –æ–¥–Ω–æ —á–∏—Å–ª–æ ‚Äì –Ω–æ–º–µ—Ä —á–µ–ª–æ–≤–µ–∫–∞, –∫–æ—Ç–æ—Ä—ã–π –æ—Å—Ç–∞–Ω–µ—Ç—Å—è –≤ –∫—Ä—É–≥—É –ø–æ—Å–ª–µ–¥–Ω–∏–º.

n, k = int(input()), int(input())
res = 0
for i in range(1, n + 1):
    res = (res + k) % i
print(res + 1)

# 9. –î–∞–Ω –Ω–∞–±–æ—Ä —Ç–æ—á–µ–∫ –Ω–∞ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–Ω–æ–π –ø–ª–æ—Å–∫–æ—Å—Ç–∏. –ù–µ–æ–±—Ö–æ–¥–∏–º–æ –ø–æ–¥—Å—á–∏—Ç–∞—Ç—å –∏ –≤—ã–≤–µ—Å—Ç–∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ç–æ—á–µ–∫, –ª–µ–∂–∞—â–∏—Ö –≤ –∫–∞–∂–¥–æ–π –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–Ω–æ–π —á–µ—Ç–≤–µ—Ä—Ç–∏.
# 
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –í –ø–µ—Ä–≤–æ–π —Å—Ç—Ä–æ–∫–µ –∑–∞–ø–∏—Å–∞–Ω–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ç–æ—á–µ–∫. –ö–∞–∂–¥–∞—è —Å–ª–µ–¥—É—é—â–∞—è —Å—Ç—Ä–æ–∫–∞ —Å–æ—Å—Ç–æ–∏—Ç –∏–∑ –¥–≤—É—Ö —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª ‚Äî –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç —Ç–æ—á–∫–∏ 
#    (—Å–Ω–∞—á–∞–ª–∞ –∞–±—Å—Ü–∏—Å—Å–∞ ‚Äì x, –∑–∞—Ç–µ–º –æ—Ä–¥–∏–Ω–∞—Ç–∞ ‚Äì y), —Ä–∞–∑–¥–µ–ª–µ–Ω–Ω—ã—Ö —Å–∏–º–≤–æ–ª–æ–º –ø—Ä–æ–±–µ–ª–∞.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ç–æ—á–µ–∫, –ª–µ–∂–∞—â–∏—Ö –≤ –∫–∞–∂–¥–æ–π –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–Ω–æ–π —á–µ—Ç–≤–µ—Ä—Ç–∏, –∫–∞–∫ –≤ –ø—Ä–∏–º–µ—Ä–∞—Ö.
#    –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –£—á—Ç–∏—Ç–µ, —á—Ç–æ —Ç–æ—á–∫–∏, –ª–µ–∂–∞—â–∏–µ –Ω–∞ –æ—Å—è—Ö –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç, –Ω–µ –ø—Ä–∏–Ω—è—Ç–æ –æ—Ç–Ω–æ—Å–∏—Ç—å –∫ –∫–∞–∫–æ–π-–ª–∏–±–æ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–Ω–æ–π —á–µ—Ç–≤–µ—Ä—Ç–∏.

result = {
    "1": 0,
    "2": 0,
    "3": 0,
    "4": 0
    }
for _ in range(int(input())):
    x, y = map(int, input().split())
    if x == 0 or y == 0:
        continue
    elif x > 0 and y > 0:
        result["1"] += 1
    elif x < 0 and y > 0:
        result["2"] += 1        
    elif x < 0 and y < 0:
        result["3"] += 1
    else:
        result["4"] += 1
print(f"""–ü–µ—Ä–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: {result["1"]}
–í—Ç–æ—Ä–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: {result["2"]}
–¢—Ä–µ—Ç—å—è —á–µ—Ç–≤–µ—Ä—Ç—å: {result["3"]}
–ß–µ—Ç–≤–µ—Ä—Ç–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: {result["4"]}""")

# 10. –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞ –∏–∑ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª. –ò–∑ –Ω–µ—ë —Ñ–æ—Ä–º–∏—Ä—É–µ—Ç—Å—è —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª. 
#     –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É –ø–æ–¥—Å—á–µ—Ç–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ —á–∏—Å–µ–ª, –∫–æ—Ç–æ—Ä—ã–µ –±–æ–ª—å—à–µ –ø—Ä–µ–¥—à–µ—Å—Ç–≤—É—é—â–µ–≥–æ –∏–º –≤ —ç—Ç–æ–º —Å–ø–∏—Å–∫–µ —á–∏—Å–ª–∞. 
# 
#     –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#     –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞ –∏–∑ —Ä–∞–∑–¥–µ–ª–µ–Ω–Ω—ã—Ö –ø—Ä–æ–±–µ–ª–∞–º–∏ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª.
#     –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#     –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –æ–¥–Ω–æ —á–∏—Å–ª–æ ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Å–ø–∏—Å–∫–∞, –±–æ–ª—å—à–∏—Ö –ø—Ä–µ–¥—ã–¥—É—â–µ–≥–æ.
#     –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –†–∞–∑–±–µ—Ä—ë–º –ø–µ—Ä–≤—ã–π —Ç–µ—Å—Ç. –£ –Ω–∞—Å —Ñ–æ—Ä–º–∏—Ä—É–µ—Ç—Å—è —Å–ø–∏—Å–æ–∫ [1, 2, 3, 4, 5], –≤ –Ω—ë–º 2>1, 3>2,  4>3, 5>4. 
#     –¢–æ –µ—Å—Ç—å –ø–æ–ª—É—á–∞–µ—Ç—Å—è 4 —Ç–∞–∫–∏—Ö —Å–ª—É—á–∞—è, –≥–¥–µ —Ç–µ–∫—É—â–µ–µ —á–∏—Å–ª–æ –±–æ–ª—å—à–µ –ø—Ä–µ–¥—ã–¥—É—â–µ–≥–æ. –ü–æ—ç—Ç–æ–º—É –æ—Ç–≤–µ—Ç 4.

lst = list(map(int, input().split()))
count = 0
for i in range(len(lst)):
    if i != 0 and lst[i] > lst[i - 1]:
        count += 1
print(count)

# 11. –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞ –∏–∑ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª. –ò–∑ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Å—Ç—Ä–æ–∫–∏ —Ñ–æ—Ä–º–∏—Ä—É–µ—Ç—Å—è —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª. 
#     –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –º–µ–Ω—è–µ—Ç –º–µ—Å—Ç–∞–º–∏ —Å–æ—Å–µ–¥–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç—ã —Å–ø–∏—Å–∫–∞ (a[0] c a[1], a[2] c a[3] –∏ —Ç.–¥.). 
#     –ï—Å–ª–∏ –≤ —Å–ø–∏—Å–∫–µ –Ω–µ—á–µ—Ç–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–º–µ–Ω—Ç–æ–≤, —Ç–æ –ø–æ—Å–ª–µ–¥–Ω–∏–π –æ—Å—Ç–∞–µ—Ç—Å—è –Ω–∞ —Å–≤–æ–µ–º –º–µ—Å—Ç–µ.
# 
#     –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#     –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞, —Å–æ–¥–µ—Ä–∂–∞—â–∞—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã–µ —á–∏—Å–ª–∞, —Ä–∞–∑–¥–µ–ª–µ–Ω–Ω—ã–µ –ø—Ä–æ–±–µ–ª–∞–º–∏.
#     –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#     –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –∏–∑–º–µ–Ω–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫, —Ä–∞–∑–¥–µ–ª—è—è –µ–≥–æ —ç–ª–µ–º–µ–Ω—Ç—ã –æ–¥–Ω–∏–º –ø—Ä–æ–±–µ–ª–æ–º.

lst = list(map(int, input().split()))
for i in range(1, len(lst), 2):
    lst[i], lst[i - 1] = lst[i - 1], lst[i]
print(*lst)

