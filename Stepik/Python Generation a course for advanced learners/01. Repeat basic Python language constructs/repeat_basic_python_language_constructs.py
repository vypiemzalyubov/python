# 1. –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —Ü–µ–ª—ã—Ö —á–∏—Å–ª–∞ a –∏ b. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—ã–≤–æ–¥–∏—Ç:
#    - —Å—É–º–º—É —á–∏—Å–µ–ª a –∏ b;
#    - —Ä–∞–∑–Ω–æ—Å—Ç—å —á–∏—Å–µ–ª a –∏ b;
#    - –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ —á–∏—Å–µ–ª a –∏ b;
#    - —á–∞—Å—Ç–Ω–æ–µ —á–∏—Å–µ–ª a –∏ b;
#    - —Ü–µ–ª—É—é —á–∞—Å—Ç—å –æ—Ç –¥–µ–ª–µ–Ω–∏—è —á–∏—Å–ª–∞ a –Ω–∞ b;
#    - –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è —á–∏—Å–ª–∞ a –Ω–∞ b;
#    - –∫–æ—Ä–µ–Ω—å –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–π –∏–∑ —Å—É–º–º—ã –∏—Ö 10-—Ö —Å—Ç–µ–ø–µ–Ω–µ–π.
#    
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —Ü–µ–ª—ã—Ö —á–∏—Å–ª–∞ a –∏ b (b –Ω–µ —Ä–∞–≤–Ω–æ 0), –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –æ–ø–µ—Ä–∞—Ü–∏–π –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.

a, b = int(input()), int(input())
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print((a ** 10 + b ** 10) ** 0.5)

# 2. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É –¥–ª—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –∏ –æ—Ü–µ–Ω–∫–∏ –∏–Ω–¥–µ–∫—Å–∞ –º–∞—Å—Å—ã —Ç–µ–ª–∞ (–ò–ú–¢) —á–µ–ª–æ–≤–µ–∫–∞. –ò–ú–¢ –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç –≤–µ—Å–∏—Ç —á–µ–ª–æ–≤–µ–∫ –±–æ–ª—å—à–µ –∏–ª–∏ –º–µ–Ω—å—à–µ –Ω–æ—Ä–º—ã –¥–ª—è —Å–≤–æ–µ–≥–æ —Ä–æ—Å—Ç–∞.
#    –ò–ú–¢ —á–µ–ª–æ–≤–µ–∫–∞ —Ä–∞—Å—Å—á–∏—Ç—ã–≤–∞—é—Ç –ø–æ —Ñ–æ—Ä–º—É–ª–µ: –ò–ú–¢ = –º–∞—Å—Å–∞ (–∫–≥) / —Ä–æ—Å—Ç(–º) * —Ä–æ—Å—Ç(–º), –≥–¥–µ –º–∞—Å—Å–∞ –∏–∑–º–µ—Ä—è–µ—Ç—Å—è –≤ –∫–∏–ª–æ–≥—Ä–∞–º–º–∞—Ö, –∞ —Ä–æ—Å—Ç ‚Äî –≤ –º–µ—Ç—Ä–∞—Ö.
#    –ú–∞—Å—Å–∞ —á–µ–ª–æ–≤–µ–∫–∞ —Å—á–∏—Ç–∞–µ—Ç—Å—è –æ–ø—Ç–∏–º–∞–ª—å–Ω–æ–π, –µ—Å–ª–∏ –µ–≥–æ –ò–ú–¢ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –º–µ–∂–¥—É 18.5 –∏ 25. –ï—Å–ª–∏ –ò–ú–¢ –º–µ–Ω—å—à–µ 18.5, —Ç–æ —Å—á–∏—Ç–∞–µ—Ç—Å—è, —á—Ç–æ —á–µ–ª–æ–≤–µ–∫ –≤–µ—Å–∏—Ç –Ω–∏–∂–µ –Ω–æ—Ä–º—ã. 
#    –ï—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –ò–ú–¢ –±–æ–ª—å—à–µ 25, —Ç–æ —Å—á–∏—Ç–∞–µ—Ç—Å—è, —á—Ç–æ —á–µ–ª–æ–≤–µ–∫ –≤–µ—Å–∏—Ç –±–æ–ª—å—à–µ –Ω–æ—Ä–º—ã.
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ "–û–ø—Ç–∏–º–∞–ª—å–Ω–∞—è –º–∞—Å—Å–∞", –µ—Å–ª–∏ –ò–ú–¢ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –º–µ–∂–¥—É 18.5 –∏ 25 (–≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ). "–ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–∞—è –º–∞—Å—Å–∞", –µ—Å–ª–∏ –ò–ú–¢ –º–µ–Ω—å—à–µ 18.5 
#    –∏ "–ò–∑–±—ã—Ç–æ—á–Ω–∞—è –º–∞—Å—Å–∞", –µ—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –ò–ú–¢ –±–æ–ª—å—à–µ 25.
# 
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –¥–≤–∞ —á–∏—Å–ª–∞: –º–∞—Å—Å–∞ –∏ —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞, –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ. 
#    –í—Å–µ –≤—Ö–æ–¥–Ω—ã–µ —á–∏—Å–ª–∞ —è–≤–ª—è—é—Ç—Å—è –≤–µ—â–µ—Å—Ç–≤–µ–Ω–Ω—ã–º–∏, –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –¥–ª—è –Ω–∏—Ö —Ç–∏–ø –¥–∞–Ω–Ω—ã—Ö float.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ç–µ–∫—Å—Ç –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.

weight, height = float(input()), float(input())
imt = weight / (height ** 2)
if 18.5 <= imt <= 25:
    print('–û–ø—Ç–∏–º–∞–ª—å–Ω–∞—è –º–∞—Å—Å–∞')
elif imt < 18.5:
    print('–ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–∞—è –º–∞—Å—Å–∞')
else:
    print('–ò–∑–±—ã—Ç–æ—á–Ω–∞—è –º–∞—Å—Å–∞')

# 3. –î–∞–Ω–∞ —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É –¥–ª—è –ø–æ–¥—Å—á–µ—Ç–∞ —Å—Ç–æ–∏–º–æ—Å—Ç–∏ —Å—Ç—Ä–æ–∫–∏, –∏—Å—Ö–æ–¥—è –∏–∑ —Ç–æ–≥–æ, —á—Ç–æ –æ–¥–∏–Ω –ª—é–±–æ–π —Å–∏–º–≤–æ–ª (–≤ —Ç–æ–º —á–∏—Å–ª–µ –ø—Ä–æ–±–µ–ª) —Å—Ç–æ–∏—Ç 60 –∫–æ–ø–µ–µ–∫. 
#    –û—Ç–≤–µ—Ç –¥–∞–π—Ç–µ –≤ —Ä—É–±–ª—è—Ö –∏ –∫–æ–ø–µ–π–∫–∞—Ö –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å –ø—Ä–∏–º–µ—Ä–∞–º–∏.
# 
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Å—Ç–æ–∏–º–æ—Å—Ç—å —Å—Ç—Ä–æ–∫–∏.
    
s = input()
cost = len(s) * 60
rouble = cost // 100
penny = cost % 100
print(f'{rouble} —Ä. {penny} –∫–æ–ø.')

# 4. –î–∞–Ω–∞ —Å—Ç—Ä–æ–∫–∞, —Å–æ—Å—Ç–æ—è—â–∞—è –∏–∑ —Å–ª–æ–≤, —Ä–∞–∑–¥–µ–ª–µ–Ω–Ω—ã—Ö –ø—Ä–æ–±–µ–ª–∞–º–∏. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –ø–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–ª–æ–≤ –≤ —ç—Ç–æ–π —Å—Ç—Ä–æ–∫–µ.
# 
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–ª–æ–≤ –≤ —Å—Ç—Ä–æ–∫–µ.
#    –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –ö—Ä–æ–º–µ —Å–ª–æ–≤ –≤ —Ç–µ–∫—Å—Ç–µ –º–æ–≥—É—Ç –ø—Ä–∏—Å—É—Ç—Å—Ç–≤–æ–≤–∞—Ç—å —Ç–æ–ª—å–∫–æ –ø—Ä–æ–±–µ–ª—ã (–æ–¥–∏–Ω –∏–ª–∏ –Ω–µ—Å–∫–æ–ª—å–∫–æ).
#    –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –†–µ—à–∏—Ç–µ –∑–∞–¥–∞—á—É –≤ –æ–¥–Ω—É —Å—Ç—Ä–æ—á–∫—É –∫–æ–¥–∞ üòé.

print(len(input().split()))

# 5. –ö–∏—Ç–∞–π—Å–∫–∏–π –≥–æ—Ä–æ—Å–∫–æ–ø –Ω–∞–∑–Ω–∞—á–∞–µ—Ç –∂–∏–≤–æ—Ç–Ω—ã–º –≥–æ–¥—ã –≤ 12-–ª–µ—Ç–Ω–µ–º —Ü–∏–∫–ª–µ. –û–¥–∏–Ω 12-–ª–µ—Ç–Ω–∏–π —Ü–∏–∫–ª –ø–æ–∫–∞–∑–∞–Ω –≤ —Ç–∞–±–ª–∏—Ü–µ –Ω–∏–∂–µ. –¢–∞–∫–∏–º –æ–±—Ä–∞–∑–æ–º, 2012 –≥–æ–¥ –±—É–¥–µ—Ç –æ—á–µ—Ä–µ–¥–Ω—ã–º –≥–æ–¥–æ–º –¥—Ä–∞–∫–æ–Ω–∞.
#    –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è —Å—á–∏—Ç—ã–≤–∞–µ—Ç –≥–æ–¥ –∏ –æ—Ç–æ–±—Ä–∞–∂–∞–µ—Ç –Ω–∞–∑–≤–∞–Ω–∏–µ —Å–≤—è–∑–∞–Ω–Ω–æ–≥–æ —Å –Ω–∏–º –∂–∏–≤–æ—Ç–Ω–æ–≥–æ. –í–∞—à–∞ –ø—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ —Ä–∞–±–æ—Ç–∞—Ç—å —Å –ª—é–±—ã–º –≥–æ–¥–æ–º, –Ω–µ —Ç–æ–ª—å–∫–æ —Ç–µ–º–∏, —á—Ç–æ –ø–µ—Ä–µ—á–∏—Å–ª–µ–Ω—ã –≤ —Ç–∞–±–ª–∏—Ü–µ.
# 
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –æ–¥–Ω–æ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ ‚Äì –≥–æ–¥.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ç–µ–∫—Å—Ç ‚Äì –Ω–∞–∑–≤–∞–Ω–∏–µ –∂–∏–≤–æ—Ç–Ω–æ–≥–æ.

match int(input()) % 12:
    case 8: print("–î—Ä–∞–∫–æ–Ω")
    case 9: print("–ó–º–µ—è")
    case 10: print("–õ–æ—à–∞–¥—å")
    case 11: print("–û–≤—Ü–∞")
    case 0: print("–û–±–µ–∑—å—è–Ω–∞")
    case 1: print("–ü–µ—Ç—É—Ö")
    case 2: print("–°–æ–±–∞–∫–∞")
    case 3: print("–°–≤–∏–Ω—å—è")
    case 4: print("–ö—Ä—ã—Å–∞")
    case 5: print("–ë—ã–∫")
    case 6: print("–¢–∏–≥—Ä")
    case 7: print("–ó–∞—è—Ü")

# 6. –î–∞–Ω–æ –ø—è—Ç–∏–∑–Ω–∞—á–Ω–æ–µ –∏–ª–∏ —à–µ—Å—Ç–∏–∑–Ω–∞—á–Ω–æ–µ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –∏–∑–º–µ–Ω–∏—Ç –ø–æ—Ä—è–¥–æ–∫ –µ–≥–æ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö –ø—è—Ç–∏ —Ü–∏—Ñ—Ä –Ω–∞ –æ–±—Ä–∞—Ç–Ω—ã–π.
# 
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –æ–¥–Ω–æ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ –ø—è—Ç–∏–∑–Ω–∞—á–Ω–æ–µ –∏–ª–∏ —à–µ—Å—Ç–∏–∑–Ω–∞—á–Ω–æ–µ —á–∏—Å–ª–æ.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –ø–æ–ª—É—á–∏—Ç—Å—è –≤ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–µ —Ä–∞–∑–≤–æ—Ä–æ—Ç–∞, —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ –≤ —É—Å–ª–æ–≤–∏–∏ –∑–∞–¥–∞—á–∏. –ß–∏—Å–ª–æ –Ω—É–∂–Ω–æ –≤—ã–≤–æ–¥–∏—Ç—å –±–µ–∑ –Ω–µ–∑–Ω–∞—á–∞—â–∏—Ö –Ω—É–ª–µ–π.
    
if len(x := input()) == 5:
    print(x[::-1].lstrip("0"))
else:
    print((x[0] + x[:0:-1]).lstrip("0"))

# 7. –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—ë—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—Å—Ç–∞–≤–ª—è–µ—Ç –≤ –∑–∞–¥–∞–Ω–Ω–æ–µ —á–∏—Å–ª–æ –∑–∞–ø—è—Ç—ã–µ 
#    –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å–æ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–º –∞–º–µ—Ä–∏–∫–∞–Ω—Å–∫–∏–º —Å–æ–≥–ª–∞—à–µ–Ω–∏–µ–º –æ –∑–∞–ø—è—Ç—ã—Ö –≤ –±–æ–ª—å—à–∏—Ö —á–∏—Å–ª–∞—Ö.
# 
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—ë—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n,(0 < n < 10**100).
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —á–∏—Å–ª–æ —Å –∑–∞–ø—è—Ç—ã–º–∏ –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.
    
