#     –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –°–∞–º–æ –Ω–∞ —Å–µ–±—è —á–∏—Å–ª–æ –∏–∑ –Ω–∞–±–æ—Ä–∞ —É–º–Ω–æ–∂–∏—Ç—å—Å—è –Ω–µ –º–æ–∂–µ—Ç. –î—Ä—É–≥–∏–º–∏ —Å–ª–æ–≤–∞–º–∏, –¥–≤–∞ –º–Ω–æ–∂–∏—Ç–µ–ª—è –¥–æ–ª–∂–Ω—ã –∏–º–µ—Ç—å —Ä–∞–∑–Ω—ã–µ –∏–Ω–¥–µ–∫—Å—ã –≤ –Ω–∞–±–æ—Ä–µ.
#     –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –î–ª—è —Ä–µ—à–µ–Ω–∏—è –∑–∞–¥–∞—á–∏ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –≤–ª–æ–∂–µ–Ω–Ω—ã–µ —Ü–∏–∫–ª—ã.

l = [int(input()) for _ in range(int(input()))]
x = int(input())
flag = False
for i in range(len(l)):
    for j in range(len(l)):
        if i == j:
            continue
        if l[i] * l[j] == x:
            flag = True
            break
print('–î–ê') if flag else print('–ù–ï–¢')

