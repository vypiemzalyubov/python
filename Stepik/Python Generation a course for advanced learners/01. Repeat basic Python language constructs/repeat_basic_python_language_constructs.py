#    - –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è —á–∏—Å–ª–∞ a –Ω–∞ b;
#    - –∫–æ—Ä–µ–Ω—å –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–π –∏–∑ —Å—É–º–º—ã –∏—Ö 10-—Ö —Å—Ç–µ–ø–µ–Ω–µ–π.
#    
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —Ü–µ–ª—ã—Ö —á–∏—Å–ª–∞ a –∏ b(b –Ω–µ —Ä–∞–≤–Ω–æ 0), –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –æ–ø–µ—Ä–∞—Ü–∏–π –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.

a, b = int(input()), int(input())
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print((a ** 10 + b ** 10) ** 0.5)

# 2. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É –¥–ª—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –∏ –æ—Ü–µ–Ω–∫–∏ –∏–Ω–¥–µ–∫—Å–∞ –º–∞—Å—Å—ã —Ç–µ–ª–∞ (–ò–ú–¢) —á–µ–ª–æ–≤–µ–∫–∞. –ò–ú–¢ –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç –≤–µ—Å–∏—Ç —á–µ–ª–æ–≤–µ–∫ –±–æ–ª—å—à–µ –∏–ª–∏ –º–µ–Ω—å—à–µ –Ω–æ—Ä–º—ã –¥–ª—è —Å–≤–æ–µ–≥–æ —Ä–æ—Å—Ç–∞.
#    –ò–ú–¢ —á–µ–ª–æ–≤–µ–∫–∞ —Ä–∞—Å—Å—á–∏—Ç—ã–≤–∞—é—Ç –ø–æ —Ñ–æ—Ä–º—É–ª–µ: –ò–ú–¢ = –º–∞—Å—Å–∞ (–∫–≥) / —Ä–æ—Å—Ç(–º) * —Ä–æ—Å—Ç(–º), –≥–¥–µ –º–∞—Å—Å–∞ –∏–∑–º–µ—Ä—è–µ—Ç—Å—è –≤ –∫–∏–ª–æ–≥—Ä–∞–º–º–∞—Ö, –∞ —Ä–æ—Å—Ç ‚Äî –≤ –º–µ—Ç—Ä–∞—Ö.
#    –ú–∞—Å—Å–∞ —á–µ–ª–æ–≤–µ–∫–∞ —Å—á–∏—Ç–∞–µ—Ç—Å—è –æ–ø—Ç–∏–º–∞–ª—å–Ω–æ–π, –µ—Å–ª–∏ –µ–≥–æ –ò–ú–¢ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –º–µ–∂–¥—É 18.5 –∏ 25. –ï—Å–ª–∏ –ò–ú–¢ –º–µ–Ω—å—à–µ 18.5, —Ç–æ —Å—á–∏—Ç–∞–µ—Ç—Å—è, —á—Ç–æ —á–µ–ª–æ–≤–µ–∫ –≤–µ—Å–∏—Ç –Ω–∏–∂–µ –Ω–æ—Ä–º—ã. 
#    –ï—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –ò–ú–¢ –±–æ–ª—å—à–µ 25, —Ç–æ —Å—á–∏—Ç–∞–µ—Ç—Å—è, —á—Ç–æ —á–µ–ª–æ–≤–µ–∫ –≤–µ—Å–∏—Ç –±–æ–ª—å—à–µ –Ω–æ—Ä–º—ã.
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ "–û–ø—Ç–∏–º–∞–ª—å–Ω–∞—è –º–∞—Å—Å–∞", –µ—Å–ª–∏ –ò–ú–¢ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –º–µ–∂–¥—É 18.5 –∏ 25 (–≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ). "–ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–∞—è –º–∞—Å—Å–∞", –µ—Å–ª–∏ –ò–ú–¢ –º–µ–Ω—å—à–µ 18.5 
#    –∏ "–ò–∑–±—ã—Ç–æ—á–Ω–∞—è –º–∞—Å—Å–∞", –µ—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –ò–ú–¢ –±–æ–ª—å—à–µ 25.
# 
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –¥–≤–∞ —á–∏—Å–ª–∞: –º–∞—Å—Å–∞ –∏ —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞, –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ. 
#    –í—Å–µ –≤—Ö–æ–¥–Ω—ã–µ —á–∏—Å–ª–∞ —è–≤–ª—è—é—Ç—Å—è –≤–µ—â–µ—Å—Ç–≤–µ–Ω–Ω—ã–º–∏, –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –¥–ª—è –Ω–∏—Ö —Ç–∏–ø –¥–∞–Ω–Ω—ã—Ö float.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ç–µ–∫—Å—Ç –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.

weight, height = float(input()), float(input())
imt = weight / (height ** 2)
if 18.5 <= imt <= 25:
    print('–û–ø—Ç–∏–º–∞–ª—å–Ω–∞—è –º–∞—Å—Å–∞')
elif imt < 18.5:
    print('–ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–∞—è –º–∞—Å—Å–∞')
else:
    print('–ò–∑–±—ã—Ç–æ—á–Ω–∞—è –º–∞—Å—Å–∞')

# 3. –î–∞–Ω–∞ —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É –¥–ª—è –ø–æ–¥—Å—á–µ—Ç–∞ —Å—Ç–æ–∏–º–æ—Å—Ç–∏ —Å—Ç—Ä–æ–∫–∏, –∏—Å—Ö–æ–¥—è –∏–∑ —Ç–æ–≥–æ, —á—Ç–æ –æ–¥–∏–Ω –ª—é–±–æ–π —Å–∏–º–≤–æ–ª (–≤ —Ç–æ–º —á–∏—Å–ª–µ –ø—Ä–æ–±–µ–ª) —Å—Ç–æ–∏—Ç 60 –∫–æ–ø–µ–µ–∫. 
#    –û—Ç–≤–µ—Ç –¥–∞–π—Ç–µ –≤ —Ä—É–±–ª—è—Ö –∏ –∫–æ–ø–µ–π–∫–∞—Ö –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å –ø—Ä–∏–º–µ—Ä–∞–º–∏.
#    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞.
#    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Å—Ç–æ–∏–º–æ—Å—Ç—å —Å—Ç—Ä–æ–∫–∏.
    
