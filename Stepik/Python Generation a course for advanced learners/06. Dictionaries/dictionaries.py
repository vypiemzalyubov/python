#      –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –ú–∏–Ω–∏-—Å–ª–æ–≤–∞—Ä—å –¥–ª—è –Ω–∞—á–∏–Ω–∞—é—â–∏—Ö —Ä–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ –º–æ–∂–Ω–æ –ø–æ—Å–º–æ—Ç—Ä–µ—Ç—å —Ç—É—Ç.
#      –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –ì–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç—Å—è, —á—Ç–æ –≤ –æ–ø—Ä–µ–¥–µ–ª—è–µ–º–æ–º —Å–ª–æ–≤–µ –∏–ª–∏ —Ñ—Ä–∞–∑–µ –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –¥–≤–æ–µ—Ç–æ—á–∏–µ (:), —Å–ª–µ–¥–æ–º –∑–∞ –∫–æ—Ç–æ—Ä—ã–º –∏–¥—ë—Ç –ø—Ä–æ–±–µ–ª.

words = {}
for _ in range(int(input())):
    k, v = input().split(':')
    words[k.lower()] = v.strip(' ')
for _ in range(int(input())):
    print(words.get(input().lower(), '–ù–µ –Ω–∞–π–¥–µ–Ω–æ'))

# 147. –ê–Ω–∞–≥—Ä–∞–º–º–∞ ‚Äì —Å–ª–æ–≤–æ (—Å–ª–æ–≤–æ—Å–æ—á–µ—Ç–∞–Ω–∏–µ), –æ–±—Ä–∞–∑–æ–≤–∞–Ω–Ω–æ–µ –ø—É—Ç—ë–º –ø–µ—Ä–µ—Å—Ç–∞–Ω–æ–≤–∫–∏ –±—É–∫–≤, —Å–æ—Å—Ç–∞–≤–ª—è—é—â–∏—Ö –¥—Ä—É–≥–æ–µ —Å–ª–æ–≤–æ (–∏–ª–∏ —Å–ª–æ–≤–æ—Å–æ—á–µ—Ç–∞–Ω–∏–µ). 
#      –ù–∞–ø—Ä–∏–º–µ—Ä, –∞–Ω–≥–ª–∏–π—Å–∫–∏–µ —Å–ª–æ–≤–∞ evil –∏ live ‚Äì —ç—Ç–æ –∞–Ω–∞–≥—Ä–∞–º–º—ã. –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —Å–ª–æ–≤–∞. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –æ–ø—Ä–µ–¥–µ–ª—è–µ—Ç, —è–≤–ª—è—é—Ç—Å—è –ª–∏ –æ–Ω–∏ –∞–Ω–∞–≥—Ä–∞–º–º–∞–º–∏.
# 
#      –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#      –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —Å–ª–æ–≤–∞, –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.
# 
#      –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#      –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ YES –µ—Å–ª–∏ —Å–ª–æ–≤–∞ —è–≤–ª—è—é—Ç—Å—è –∞–Ω–∞–≥—Ä–∞–º–º–∞–º–∏ –∏ NO –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ.

s1, s2 = input(), input()
d1, d2 = {c1: s1.count(c1) for c1 in s1}, {c2: s2.count(c2) for c2 in s2}
