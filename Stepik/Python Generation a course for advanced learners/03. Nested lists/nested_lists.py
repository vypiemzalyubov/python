# 28. –¢—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫ –ü–∞—Å–∫–∞–ª—è ‚Äî –±–µ—Å–∫–æ–Ω–µ—á–Ω–∞—è —Ç–∞–±–ª–∏—Ü–∞ –±–∏–Ω–æ–º–∏–∞–ª—å–Ω—ã—Ö –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç–æ–≤, –∏–º–µ—é—â–∞—è —Ç—Ä–µ—É–≥–æ–ª—å–Ω—É—é —Ñ–æ—Ä–º—É. –í —ç—Ç–æ–º —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–µ –Ω–∞ –≤–µ—Ä—à–∏–Ω–µ –∏ –ø–æ –±–æ–∫–∞–º —Å—Ç–æ—è—Ç –µ–¥–∏–Ω–∏—Ü—ã. –ö–∞–∂–¥–æ–µ —á–∏—Å–ª–æ —Ä–∞–≤–Ω–æ —Å—É–º–º–µ –¥–≤—É—Ö —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω–Ω—ã—Ö –Ω–∞–¥ –Ω–∏–º —á–∏—Å–µ–ª.
# 
#     0:      1
#     1:     1 1
#     2:    1 2 1
#     3:   1 3 3 1
#     4:  1 4 6 4 1
#           .....
# 
#     –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —á–∏—Å–ª–æ n. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —É–∫–∞–∑–∞–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ –ü–∞—Å–∫–∞–ª—è –≤ –≤–∏–¥–µ —Å–ø–∏—Å–∫–∞ (–Ω—É–º–µ—Ä–∞—Ü–∏—è —Å—Ç—Ä–æ–∫ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –Ω—É–ª—è).
# 
#     –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#     –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —á–∏—Å–ª–æ n (n‚â•0).
# 
#     –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
#     –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —É–∫–∞–∑–∞–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ –ü–∞—Å–∫–∞–ª—è –≤ –≤–∏–¥–µ —Å–ø–∏—Å–∫–∞.

def pascal(n):
    triangle = [[1] + [0]*n for i in range(n+1)]
    for i in range(1, n+1):
        for j in range(1, i+1):
            triangle[i][j] = triangle[i-1][j] + triangle[i-1][j-1]    
    return(triangle[-1])

print(pascal(int(input())))

