# 
#     –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ü–æ—Ä—è–¥–æ–∫ —Å–ø–∏—Å–∫–æ–≤ –æ–¥–∏–Ω–∞–∫–æ–≤–æ–π –¥–ª–∏–Ω—ã –¥–æ–ª–∂–µ–Ω —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–æ–≤–∞—Ç—å –ø–æ—Ä—è–¥–∫—É –∏—Ö –≤—Ö–æ–∂–¥–µ–Ω–∏—è –≤ –æ—Å–Ω–æ–≤–Ω–æ–π —Å–ø–∏—Å–æ–∫.

lst = input().split()
result, add = [[]], []

for i in range(len(lst)):
    for j in range(len(lst)):
        add = lst[j:i + j + 1]
        if len(add) == i + 1:
            result.append(add)
            
print(result)

