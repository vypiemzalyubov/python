#      Current dollar rate is <курс доллара>. You want to buy <количество долларов> dollars
#      You must pay <стоимость>

(lambda rate, count: print(f'''Current dollar rate is {rate}. You want to buy {count} dollars
You must pay {rate * count}'''))(float(input()), int(input()))

